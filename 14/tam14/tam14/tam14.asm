		.INCLUDE "M32DEF.INC"
		.ORG 0
		RJMP main
		.ORG $100
main:	SBI DDRA,0
		LDI R16,249
		LDI R17,$0A ; PRESCALING 1/8
		LDI R18,$02
		SER R20
		CLR R21
		OUT OCR0,R16	
		OUT TCCR0,R17
wait:	IN R19,TIFR
		SBRS R19,1
		RJMP wait
		EOR R21,R20
		OUT PORTA,R21
		OUT TIFR,R18
		RJMP wait